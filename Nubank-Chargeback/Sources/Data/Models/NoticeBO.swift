import Foundation
import ObjectMapper

//**********************************************************************************************************
//
// MARK: - Constants -
//
//**********************************************************************************************************

//**********************************************************************************************************
//
// MARK: - Definitions -
//
//**********************************************************************************************************

//**********************************************************************************************************
//
// MARK: - Class -
//
//**********************************************************************************************************

public class NoticeBO: Mappable {

//*************************************************
// MARK: - Properties
//*************************************************
	
	public var title: String?
	public var description: String?
	public var primary_action: ActionBO?
	public var secondary_action: ActionBO?

//*************************************************
// MARK: - Constructors
//*************************************************

	public required init() { }
	
	public required init?(map: Map) { }
	
//*************************************************
// MARK: - Protected Methods
//*************************************************

//*************************************************
// MARK: - Exposed Methods
//*************************************************
	
	public func mapping(map: Map) {
		self.title <- map["title"]
		self.description <- map["description"]
		self.primary_action <- map["primary_action"]
		self.secondary_action <- map["secondary_action"]
	}

//*************************************************
// MARK: - Overridden Public Methods
//*************************************************

}

